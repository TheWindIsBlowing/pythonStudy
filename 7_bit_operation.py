
# 按位与 &
# 4 --> 00000100
# 8 --> 00001000
# ? --> 00000000  = 0  同为1则为1，否则为0
a, b = 4, 8
print(a & b)

# 按位或 |
# 4 --> 00000100
# 8 --> 00001000
# ? --> 00001100  = 12  同为0则为0，否则为1
a1, b1 = 4, 8
print(a1 | b1)

# 左移运算 <<
# 4 --> 00000100
# << 1
# ? --> 00001000  = 8  高位舍弃，低位补0
print(4 << 1) # 左移1 相当于 4 * 2 ^ 1 = 8
print(4 << 2) # 左移2 相当于 4 * 2 ^ 2 = 16

# 右移运算 >>
# 4 --> 00000100
# >> 1
# ? --> 00000010  = 2  低位舍弃，高位补0
print(4 >> 1) # 右移1 相当于 4 / 2 ^ 1 = 2
print(4 >> 2) # 右移2 相当于 4 / 2 ^ 2 = 1
print(4 >> 4)